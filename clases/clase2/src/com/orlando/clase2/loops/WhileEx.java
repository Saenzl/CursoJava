public class WhileEx {
	
	//Metodo main. corre la aplicaion
	public static void main(String[] args) {
		/*
		 * bloque while ejecuta un bloque de codigo
		 * siempre que su condicion sea verdadera.
		 * 
		 * while(condition){
		 * 		codigo a ejecutar;
		 * 		---
		 * 		---
		 * }
		 */
		
		
		int i = 1;
		while(i <= 100){
			System.out.println(i);
			i += 2;
		}
		
		
	}
}
