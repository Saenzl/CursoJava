package loops;

/**
 * 
 * @author orlando
 * ForEx Class
 */
public class ForEx {
	
	//Metodo main. corre la aplicaion
	public static void main(String[] args) {
		
		/*
		 * For loop
		 * 
		 * for(variable de iteracion; condicion ; incremento ){
		 * 		-----
		 * 		-----
		 * }
		 */
		for(int i = 1; i <= 10; i++){
			System.out.println("5 x " + i + " = " + (5 * i));
		}
		
	}
	
}
